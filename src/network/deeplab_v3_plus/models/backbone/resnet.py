from torchvision.models.resnet import BasicBlock, Bottleneck, ResNet, load_state_dict_from_url, model_urls

__all__ = ['ResNet', 'resnet18', 'resnet34', 'resnet50', 'resnet101',
           'resnet152', 'resnext50_32x4d', 'resnext101_32x8d',
           'wide_resnet50_2', 'wide_resnet101_2']


class MyResNet(ResNet):
    """
    This resnet is serves as a feature extractor. It does not go through the avgpool and fc layer.
    It will also returns the low level features. Usually low level features is the output of ResNet.layer1().
    """

    def __init__(self, *args, **kwargs):
        super(MyResNet, self).__init__(*args, **kwargs)
        self.in_channels = 3
        self.out_channels = 2048
        self.low_level_channels = 256

        # Remove the avgpool and fc layer
        delattr(self, 'avgpool')
        delattr(self, 'fc')

    def forward(self, x):
        x = self.conv1(x)
        x = self.bn1(x)
        x = self.relu(x)
        x = self.maxpool(x)

        x = self.layer1(x)
        low_features = x

        x = self.layer2(x)
        x = self.layer3(x)
        x = self.layer4(x)

        # Return a dict instead of a tensor allows more flexible outputs
        out_dict = {
            "feature": x,
            "low_feature": low_features
        }

        return out_dict


def _resnet(arch, block, layers, pretrained, progress, **kwargs):
    model = MyResNet(block, layers, **kwargs)
    if pretrained:
        state_dict = load_state_dict_from_url(model_urls[arch], progress=progress)
        state_dict.pop('fc.weight', None)
        state_dict.pop('fc.bias', None)
        model.load_state_dict(state_dict)
    return model


def resnet18(pretrained=False, progress=True, **kwargs):
    r"""ResNet-18 model from
    `"Deep Residual Learning for Image Recognition" <https://arxiv.org/pdf/1512.03385.pdf>`_

    Args:
        pretrained (bool): If True, returns a model pre-trained on ImageNet
        progress (bool): If True, displays a progress bar of the download to stderr
    """
    return _resnet('resnet18', BasicBlock, [2, 2, 2, 2], pretrained, progress,
                   **kwargs)


